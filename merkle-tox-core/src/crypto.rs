    let spk_b = StaticSecret::from(*self_signed_pre_sk.as_bytes());

    let i_a = ed25519_pk_to_x25519(peer_device_pk.as_bytes())?;
    let e_a = XPublicKey::from(*peer_ephemeral_pk.as_bytes());

    let dh1 = spk_b.diffie_hellman(&i_a);
