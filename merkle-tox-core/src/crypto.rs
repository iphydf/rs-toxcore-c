/// Keys to pack content node into wire format.
pub struct PackContentKeys {
    pub k_msg: MessageKey,       // payload encryption + sender_hint
    pub k_header: HeaderKey,     // routing AEAD
    pub routing_nonce: [u8; 12], // random, generated by caller
    pub payload_nonce: [u8; 12], // random, generated by caller
}

/// Keys for packing MerkleNode into WireNode.
pub enum PackKeys {
    /// Content nodes: encrypted routing + payload.
    Content(PackContentKeys),
    /// Admin, KeyWrap, HistoryExport, SKD: cleartext.
    Exception,
}

/// K_header_epoch_n = Blake3-KDF("merkle-tox v1 header-key", K_conv || SenderKey_n)
pub fn derive_k_header_epoch(k_conv: &KConv, sender_key_n: &SenderKey) -> HeaderKey {
    let mut material = [0u8; 64];
    material[0..32].copy_from_slice(k_conv.as_bytes());
    material[32..64].copy_from_slice(sender_key_n.as_bytes());
    let result = HeaderKey::from(derive_key("merkle-tox v1 header-key", &material));
    material.zeroize();
    result
}

/// sender_hint = Blake3-KDF("merkle-tox v1 hint", K_msg)[0..4]
pub fn compute_sender_hint(k_msg: &MessageKey) -> [u8; 4] {
    let full = derive_key("merkle-tox v1 hint", k_msg.as_bytes());
    let mut hint = [0u8; 4];
    hint.copy_from_slice(&full[0..4]);
    hint
}

/// AEAD-encrypts routing data (sequence number) under K_header.
/// Returns 24 bytes: 8 bytes ciphertext + 16 bytes Poly1305 tag.
/// Caller prepends nonce externally.
pub fn encrypt_routing_aead(
    k_header: &HeaderKey,
    nonce: &[u8; 12],
    seq: u64,
    aad: &[u8; 32],
) -> Vec<u8> {
    use chacha20poly1305::ChaCha20Poly1305;
    use chacha20poly1305::aead::{AeadInPlace, KeyInit};

    let cipher = ChaCha20Poly1305::new(k_header.as_bytes().into());
    let plaintext = seq.to_be_bytes();
    let mut buffer = plaintext.to_vec();
    let tag = cipher
        .encrypt_in_place_detached(nonce.into(), aad, &mut buffer)
        .expect("AEAD encrypt should not fail");
    buffer.extend_from_slice(&tag);
    buffer
}

/// AEAD-decrypts routing data. Returns sequence number on success.
pub fn decrypt_routing_aead(
    k_header: &HeaderKey,
    nonce: &[u8; 12],
    ciphertext: &[u8],
    aad: &[u8; 32],
) -> Option<u64> {
    use chacha20poly1305::ChaCha20Poly1305;
    use chacha20poly1305::aead::{AeadInPlace, KeyInit};

    if ciphertext.len() != 24 {
        return None;
    }

    let cipher = ChaCha20Poly1305::new(k_header.as_bytes().into());
    let (ct, tag_bytes) = ciphertext.split_at(8);
    let mut buffer = ct.to_vec();
    let tag = chacha20poly1305::Tag::from_slice(tag_bytes);
    cipher
        .decrypt_in_place_detached(nonce.into(), aad, &mut buffer, tag)
        .ok()?;
    let seq = u64::from_be_bytes(buffer.try_into().ok()?);
    Some(seq)
}

